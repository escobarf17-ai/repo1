#Coleccion
nombres = ["Ana", "Luis", "Camilo"]

for nombre in nombres:
    print(nombre)

for letra in "Python":
    print(letra)

#Diccionario
persona = {"nombre": "Camilo", "edad": 25}

for clave in persona:
    print(clave, persona[clave])

for i in range(5):
    print("Hola")

for i in range(1, 6):
    print(i)

for i in range(0, 10, 2):
    print(i)
